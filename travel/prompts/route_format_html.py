"""
Промпт для оформления маршрута (JSON) в красивый HTML для full_description.
Один вызов LLM: на входе — структура маршрута, на выходе — HTML.
"""

import json
from typing import Any


def build_route_format_html_prompt(llm_route: dict[str, Any]) -> str:
    """
    Формирует промпт для ИИ: оформить данные маршрута в читаемый HTML.
    llm_route — ответ первого LLM (route_name, description, waypoints, general_tips и т.д.).
    """
    route_json = json.dumps(llm_route, ensure_ascii=False, indent=2)

    return f"""Ты — редактор туристических текстов. Преврати данные маршрута в красивый, структурированный HTML для страницы описания.

📋 ДАННЫЕ МАРШРУТА (JSON):
{route_json}

📋 ТРЕБОВАНИЯ К HTML:
1. Верни ТОЛЬКО валидный JSON-объект с одним полем: "html". Значение — строка с HTML. Без пояснений и маркдауна.
   Пример формата ответа: {{"html": "<p>...</p>"}}

2. Структура HTML:
   - Общее описание маршрута — в начале, в абзацах <p>.
   - Для каждой точки маршрута (waypoints): заголовок уровня <h3> с номером и названием (например «Точка 1. Название»), затем <p> с описанием, рекомендуемое время (если есть), советы — списком <ul><li>, интересный факт — отдельным абзацем или <blockquote>.
   - В конце блок «Общие советы» — заголовок <h3> и список <ul><li>.
   - При необходимости блок с общим временем и лучшим временем посещения — коротко в <p>.

3. Разрешены только теги: p, h2, h3, ul, ol, li, strong, em, br, blockquote. Без script, style, div (если возможно — только семантика выше).

4. Текст внутри тегов экранируй для HTML (например &amp;, &lt;, &gt;, кавычки). Не вставляй изображения и ссылки.

5. Стиль: дружелюбный, читаемый, с логичными отступами между блоками. Заголовки точек — чётко выделены.
"""
